    private void btnEnter4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnter4ActionPerformed

        String fourthUserAns;
        final String Q4_FIRST_CORRECT_ANS = "E";
        final String Q4_SECOND_CORRECT_ANS = "e";

        // retrieve user information
        fourthUserAns = txtUserAns4.getText();

        // processing and feedback output
        if (fourthUserAns.length() == 1) { // https://stackoverflow.com/questions/49308129/java-verify-user-input-for-one-character
            // score counter
            if ((fourthUserAns.equals(Q4_FIRST_CORRECT_ANS)) || (fourthUserAns.equals(Q4_SECOND_CORRECT_ANS))) {
                lblResultStatus4.setText("Correct!");
                numQsCorrect = numQsCorrect + 1;
            } else {
                lblResultStatus4.setText("Incorrect!");
                numQsIncorrect = numQsIncorrect + 1;
            }
            // progress counter
            numQsCompleted = numQsCompleted + 1;
        } else {
            lblResultStatus4.setText("Error. You must enter an answer that is 1 character. Try again.");
        }

        // progress output
        lblValueProgress.setText(String.valueOf(numQsCompleted));
    }//GEN-LAST:event_btnEnter4ActionPerformed

