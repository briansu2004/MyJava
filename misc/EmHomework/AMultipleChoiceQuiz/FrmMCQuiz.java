    private void btnOverallStatsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOverallStatsActionPerformed

        // calculation
        percentageCorrect = ((double) numQsCorrect / (double) NUMBER_OF_QUESTIONS) * 100;

        // rounding
        percentageCorrectRounded = Math.round((percentageCorrect) * 10) / 10.0; // https://stackoverflow.com/questions/22186778/using-math-round-to-round-to-one-decimal-place

        // output
        lblValueQsCorrect.setText(String.valueOf(numQsCorrect));
        lblValueQsIncorrect.setText(String.valueOf(numQsIncorrect));
        lblValuePercentage.setText(String.valueOf(percentageCorrectRounded));
    }//GEN-LAST:event_btnOverallStatsActionPerformed

